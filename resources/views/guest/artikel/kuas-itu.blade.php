        <p style="text-indent:2.5em;">
            KUAS ITU (Kuis Asik Instagram Terpadu) merupakan salah satu program kerja dari divisi Akademik dengan mengunggah soal melalui Instagram STIS 62.
            Mata kuliah yang dijadikan dasar dalam pembuatan soal adalah mata kuliah hasil voting dari mahasiswa angkatan 62 yang dilakukan setiap awal semester melalui jarkoman.
            Pada voting yang dilakukan pada Kamis, 17 Februari 2022 dan Selasa, 19 April 2022, didapatkan hasil voting mata kuliah yang dijadikan dasar dalam pembuatan soal adalah Metode Numerik, Basis Data, Komputasi Statistik, dan Analisis Regresi.
        </p>
        <br>
        <p style="text-indent:2.5em;">
            Terdapat satu soal untuk tiap mata kuliah dengan jawaban yang berupa isian singkat. Penentuan pemenang didasarkan pada penjawab soal benar yang tercepat.
            Kuis pertama dilaksanakan pada 12 Maret 2022, kuis kedua dilaksanakan pada 2 April 2022, dan seterusnya kuis akan dilaksanakan setiap satu bulan sekali.
            Pembahasan soal dibagikan ke angkatan 62 sehari setelah kuis ini dilaksanakan. Sampai bertemu di KUAS ITU selanjutnya!
        </p>

        <img src="../style/artikel/doa-bersama-uts-genap-21-22/img/headline.png" alt="Doa Bersama" title="Doa Bersama">
        <br>
        <br>
        <p style="text-indent:2.5em;">Kegiatan Doa Bersama merupakan kegiatan yang rutin diadakan secara bersama oleh kepengurusan angkatan. Kegiatan ini ditujukan untuk seluruh angkatan baik angkatan 63, 62, 61, maupun 60 sebelum menghadapi ujian, baik itu Ujian Tengah Semester (UTS) maupun Ujian Akhir Semester (UAS).</p>
        <br>
        <p style="text-indent:2.5em;">Pada tahun 2022 ini, doa bersama pernah diadakan sekali. Doa bersama tersebut dilaksanakan pada hari Jumat, 1 April 2022 hingga hari Sabtu, 2 April 2022. Kegiatan doa bersama ini dilakukan agar mahasiswa memiliki kesiapan secara mental dan spiritual sebelum menghadapi Ujian Tengah Semester di semester genap tahun ajaran 2021/2022. </p>
        <br>
        <img src="../style/artikel/doa-bersama-uts-genap-21-22/img/rohis1.png" alt="Dokumentasi Doa Bersama Rohis (1)" title="Dokumentasi Doa Bersama Rohis (1)">
        <br>
        <br>
        <img src="../style/artikel/doa-bersama-uts-genap-21-22/img/rohis2.png" alt="Dokumentasi Doa Bersama Rohis (2)" title="Dokumentasi Doa Bersama Rohis (2)">
        <br>
        <br>
        <p style="text-indent:2.5em;">Pada hari Jum'at, kegiatan doa bersama dilaksanakan bagi mahasiswa yang bergama kristen. Doa bersama ini dipimpin oleh Mas Pur, seorang tokoh agama dari luar Polstat STIS. Sedangkan bagi mahasiswa yang beragama islam dan hindu, doa bersama dilakukan pada hari sabtu yang tentunya di ruangan zoom yang terpisah. Doa bersama untuk mahasiswa yang beragama islam dipimpin oleh Ustadz M. Ridwan Yahya. Sementara itu, doa bersama untuk mahasiswa yang bergama hindu dipimpin oleh ketua Rohin, yakni Kak Apta.</p>
        <br>
        <p style="text-indent:2.5em;">Dalam kegiatan ini harapannya kita sebagai manusia tidak hanya melakukan usaha tetapi juga tetap berdoa dan ingat kepada Tuhan Yang Maha Esa untuk memohon pertolongan supaya diberi kemudahan dan kelancaran ketika pelaksanaan ujian. Kita harus ingat bahwa tugas manusia hanyalah berusaha. Hasil dari usaha kita itu hanya Tuhan yang bisa menentukan. Oleh karena itu, kita juga wajib untuk memohon kepada Tuhan agar diberikan hasil yang terbaik. Melalui kegiatan doa bersama ini, diharapkan dapat membuat teman-teman semua sudah siap melaksanakan ujian baik secara materi, mental, maupun spiritual.</p>

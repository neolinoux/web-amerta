    <!-- Konten artikel -->
    <article>
        <img src="../style/artikel/mean-data/img/meandata.jpg" alt="">
        <br>
        <br>
        <p style="text-indent: 2.5em;">Pada 11 Maret 2022, 13 Maret 2022, 13 Mei 2022, dan 20 Mei 2022 pukul 19.30 WIB telah
            dilaksanakan Mean Data yang dihadiri oleh lima atau enam kelas di tingkat 2. Kegiatan ini bertujuan
            untuk saling mengenal antar internal angkatan Amerta 62. Adapun tujuan dari adanya pembagian
            kelas di tiap episodenya selain untuk memaksimalkan dalam hal pengenalan tiap angkatan tetapi
            juga memaksimalkan SDM yang ada yaitu anggota Internal yang bertugas untuk memandu acara
            Mean Data pada sesi breakout room. Sebelum acara berlangsung, panitia telah mengirimkan poster
            dan jarkom mengenai Mean Data ke tiap kelas yang bersangkutan.</p>
        <br>
        <p style="text-indent: 2.5em;">Kegiatan ini berisikan acara obrolan asik atau bisa juga mendalam mengenai tiap peserta
            yang hadir. Sistematikanya adalah peserta Mean Data berkumpul di main room Zoom terlebih
            dahulu. Kemudian dilakukan pembukaan oleh MC seperti menyapa peserta, perkenalan ketua kelas,
            memberikan gambaran acara yang akan berlangsung, dan berdoa. Setelah itu, tiap mahasiswa
            diarahkan untuk masuk ke breakout room yang sudah diberitahu terlebih dahulu oleh panitia
            beberapa hari sebelumnya. Pada tiap Breakout Room terdapat 2 anggota internal atau humas HPD
            yang bertugas memandu jalannya acara.
        </p>
        <br>
        <p style="text-indent: 2.5em;">
            Mean Data ini lebih difokuskan pada saat sesi breakout
            room sehingga waktu yang disiapkan cukup banyak yaitu sekitar 90 menit. Di breakout room, tiap
            peserta terlebih dahulu memperkenalkan dirinya masing-masing. Format namanya seperti nama
            lengkap, nama panggilan, kelas, asal daerah, status, dan motto hidup. Setelah itu dilanjutkan dengan
            sesi Q &amp; A dengan pertanyaan-pertanyaan seru yang sudah disiapkan oleh panitia. Pertanyaan
            tersebut mulai dari perkuliahan hingga relationship. Saat waktu di breakout room berakhir, petugas
            di tiap breakout room mengakhiri sesi Q &amp; A dan ngobrol santai dan mempersilahkan peserta untuk
            kembali ke main room. Penutupan acara Mean Data di tiap episodenya dipandu oleh MC. Sebelum
            menutup acara ini, MC berinteraksi kepada peserta yang telah hadir seperti meminta feedback
            terhadap keberlangsungan acara pada malam itu. Acara ditutup dengan dokumentasi, doa, dan
            ucapan terima kasih kepada peserta yang telah hadir.</p>
        <br>
        <p style="text-indent: 2.5em;">Setelah dilaksanakan Mean Data sebanyak empat episode, peserta yang menghadiri acara ini
            sekitar 1/3 orang dari tiap kelas. Dengan berakhirnya acara Mean Data pada periode kepengurusan
            tahun ini, diharapkan tujuan dari dilaksanakannya Mean Data dapat tercapai yaitu saling mengenal
            antar angkatan 62 walaupun belum bertemu secara langsung karena adanya pandemi covid-19.
        </p>

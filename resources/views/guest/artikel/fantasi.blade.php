        <br>
        <p style="text-indent: 2.5em;"> Pada hari Sabtu, 26 Februari 2022 telah dilaksanakan Fantasi yang dihadiri oleh seluruh
            pengurus Gradasi 62. Kegiatan ini merupakan pertemuan awal Gradasi 62 yang bertujuan untuk
            mengenal serta mempererat rasa kebersamaan. Kegiatan dimulai dengan sambutan dari Ketua Tingkat Angkatan 62
            yaitu Saifullah Fatah dan
            koordinator Internal yaitu Alwan Rahmana Subian.</p>
        <br>
        <p style="text-indent: 2.5em;">Menurut Fatah, penting sebagai pengurus 62 untuk saling mengenal satu sama lain.
            Harapannya acara Fantasi tersebut suatu saat akan menjadi “Kenyataan”.Menurut Alwan, kegiatan
            ini bertujuan untuk merekatkan kepengurusan 62 yang mana kepengurusan ini adalah core atau inti
            dari angkatan 62. Beliau juga berpesan agar jangan pernah memutus silaturahmi antar sesama
            karena kita akan menjadi rekan kerja by default selama beberapa dekade.</p>
        <br>
        <p style="text-indent: 2.5em;">Pada acara ini diadakan games intra pengurus seperti game hang-man dan sebagainya.
            Sebelumnya ada video perkenalan tiap-tiap anggota Gradasi 62. Terdapat juga sesi obrolan di Break
            Room yang tersedia dan sesi Virtual Blind Date mendadak yang diadakan guna persiapan acara
            Virtual Blind Date minggu depannya.</p>
        <p style="text-indent: 2.5em;">
            Acara berlangsung selama 1-2 jam. Sebelum menutup acara, ada beberapa orang yang
            dipilih untuk ditanya mengenai kesan acara. Semuanya memberi kesimpulan yang kurang lebih
            sama: Acara berjalan dengan seru dan menyenangkan. Sebelum menutup acara, terlebih dahulu
            diadakan sesi doa dan dokumentasi.
        </p>

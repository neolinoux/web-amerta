        <p style="text-indent:2.5em;">
            Pada hari Sabtu, 12 Maret 2022 telah dilaksanakan VBD yang bertujuan untuk memperluas relasi baik dari internal maupun eksternal kampus.
            Kegiatan ini adalah kegiatan yang bertujuan untuk memperluas relasi baik intra maupun ekstra kampus. Pada kegiatan kali ini ada sekitar 30-an orang yang tergabung dari berbagai macam kampus. Ada yang dari Unja, ITB, IPB, dan STIS sendiri.
        </p>
        <br>
        <p style="text-indent:2.5em;">
            Promosi kegiatan ini telah dilakukan jauh-jauh hari. Promosi dilakukan melalui platform media sosial serta jarkoman-jarkoman yang disebar di grup kelas mahasiswa Polstat STIS.Kegiatan ini dipandu oleh MC dari pihak internal.
            Kegiatan yang ada dalam VBD ini meliputi games berupa tebak gambar dengan referensi film dan musik serta kegiatan saling mengenal yang diadakan di Breakout Room.
        </p>
        <br>
        <p style="text-indent:2.5em;">
            Acara ini ditutup dengan dokumentasi dan doa serta bila ada yang ingin mengetahui identitas lawan bicaranya di Breakout Room dapat menghubungi panitia untuk diberikan identitas yang bersangkutan (dengan consent).

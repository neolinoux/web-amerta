        <br>
        <br>
        <p style="text-indent: 2.5em;"> “Elipsis” merupakan kegiatan evaluasi dalam pengurus angkatan yang diselenggarakan oleh
            PKO. Kegiatan ini merupakan salah satu program rutin berupa penilaian program kerja yang
            telah dilaksanakan kepengurusan, evaluasi kepengurusan, serta penyampaian aspirasi.
            Penilaian, saran, dan kritik yang dibahas di Elipsis merupakan hasil dari Abstraksi (Aspirasi Bersama
            Gradasi) yang dilaksanakan pada Minggu pertama bulan Maret dan Mei 2022. Elipsis sudah
            terlaksana sebanyak dua kali, yaitu pada 24 Maret 2022 dan 21 Mei 2022.</p>
        <br>
        <p style="text-indent: 2.5em;">Kegiatan ini dihadiri oleh seluruh anggota kepengurusan bersama BPH dan PKO. Dalam
            kegiatan Elipsis, ditampilkan statistik penilaian program kerja, evaluasi kepengurusan, serta
            penyampaian kritik saran dari Angkatan 62. Masing-masing divisi terkait juga berperan aktif dalam
            menanggapi kritik dan saran tersebut. Hasil dari kegiatan ini adalah penyampaian tanggapan serta
            tindak lanjut pengurus terhadap aspirasi yang memiliki urgensi khusus.</p>
        <br>
        <p style="text-indent: 2.5em;">Dengan adanya kegiatan ini, pengurus dapat mengevaluasi berbagai program kerja yang
            sudah terlaksana. Selain itu, aspirasi dari Angkatan 62 juga dapat tersampaikan serta ditanggapi
            dengan baik.</p>


        <p style="text-indent: 2.5em;">KSM atau Kelompok Studi Mahasiswa merupakan salah satu program kerja dari divisi Akademik.
            Pada kegiatan ini, semua mahasiswa angkatan 62 belajar bersama secara virtual melalui aplikasi
            Zoom Meeting yang dipandu oleh dua orang MC untuk kelancaran acara. Selain itu, dalam pelaksanaan
            kegiatan ini ada tutor yang membantu teman-teman angkatan 62 dalam memahami dan me-review materi perkuliahan.
            Tutor KSM terdiri dari tutor sebaya dan kakak tingkat (Kating).
        </p>
        <br>
        <p style="text-indent: 2.5em;">Pada 5 â€“ 6 Maret 2022, materi perkuliahan disampaikan oleh tutor sebaya dengan 4 mata kuliah
            yang berbeda dan setiap mata kuliah disampaikan oleh dua orang tutor. Pada Sabtu, 5 Maret 2022,
            mata kuliah yang dipelajari bersama yakni Metode Numerik (D4-KS), Basis Data (D4-ST), dan Komputasi
            Statistik (D3-ST) yang terbagi menjadi 3 breakout room. Sementara itu, pada, 6 Maret 2022, mata kuliah
            yang dipelajari bersama adalah Analisis Regresi.
        </p>
        <br>
        <p style="text-indent: 2.5em;">Pada 26-27 Maret 2022, setiap materi perkuliahan disampaikan oleh satu orang tutor kating.
            Pembagian mata kuliah sama seperti KSM sebelumnya, yakni untuk hari pertama (26 Maret 2022)
            mata kuliah yang dipelajari bersama adalah Metode Numerik, Basis Data, dan Komputasi Statistik,
            sedangkan Analisis Regresi dipelajari pada hari kedua (27 Maret 2022).

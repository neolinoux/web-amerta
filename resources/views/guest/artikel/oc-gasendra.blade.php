        <br>
        <br>
        <p style="text-indent: 2.5em;">
            Pada hari Sabtu, 23 April 2022 telah terlaksana Opening Ceremony Gasendra 62. Kegiatan ini
            diadakan untuk menjadi pembuka dari seluruh rangkaian acara Gasendra 62. Gasendra sendiri
            merupakan suatu Event Pergelaran dari Keluarga Amerta yang merupakan unifikasi Pekan Kompetensi
            Online (PKO) dan Pergelaran Kreasi Seni Mahasiswa (PKSM) pada Gema Tingkat I yang lalu. Gasendra 62
            mengangkat seluruh unsur seni dan kompetensi dari dan untuk mahasiswa 62. Gasendra
            menggambarkan nilai daya cipta, naluri, dan filosofis bagi Keluarga Amerta 62.
        </p>
        <br>
        <p style="text-indent: 2.5em;">
            Tema yang diangkat dalam Gasendra adalah Smaravarna yang berarti kenangan indah dengan
            tagline “Gema Smaravarna, Semarak Suka Cita”. Kegiatan opening ceremony dihadiri oleh keempat
            ketua ORMAWA (SEMA, DPM, SPD, dan MENWA), ketua UKM Kesenian, ketua kelas beserta
            perwakilannya dan teman-teman amertarians. Acara ini dimeriahkan dengan penampilan drama yang
            dibawakan oleh ukm teater ANTIK dan penampilan band oleh ukm Xbar.
        </p>
        <br>
        <p style="text-indent: 2.5em;">
            Dengan adanya kegiatan opening Ceremony diharapkan dapat menjadi awal yang baik untuk
            rangkain acara Gasendra kedepannya. Selain itu agar teman-teman amertarians dapat lebih mengenal
            tentang Gasendra serta antusias mengikuti seluruh kegiatan Gasendra.
        </p>
